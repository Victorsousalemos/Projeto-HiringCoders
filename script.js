var colors = ['black', 'white', 'yellow', 'green', 'blue']

for (var i = 0; i < 5; i++) {
  console.log(colors[i])
}

for (var i = 1; i <= 10; i++) {
  console.log(i)
}

var i = 1

while (i <= 10) {
  console.log(i)
  i++
}

do {
  console.log(i)
  i++
} while (i < 10)

